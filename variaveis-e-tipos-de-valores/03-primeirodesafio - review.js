// Pedir para o usuário digitar um número
let nu1 = prompt('Digite um número')

// converter em número inteiro
nu1 = parseInt(nu1)

// Pedir para o usuário digitar outro número
let nu2 = prompt('Digite outro número')

// converter em número inteiro
nu2 = parseInt(nu2)

// mostrar a divisão contatenando com o texto "A divisão é ..."
alert('A divisão é: ' + (nu1 / nu2))

// mostrar a multiplicar contatenando com o texto "A multiplicação é ..."
alert('A multiplicação é: ' + (nu1 * nu2))

// mostrar a subtração contatenando com o texto "A subtração é ..."
alert('A subtração é: ' + (nu1 - nu2))

// mostrar a soma contatenando com o texto "A soma é ..."
alert('A soma é: ' + (nu1 + nu2))

